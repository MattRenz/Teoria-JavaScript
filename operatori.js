/*

1 Operatori di assegnazione:
    =: Assegna un valore ad una variabile.
    +=: Aggiunge e assegna un valore.
    -=: Sottrae e assegna un valore.
    *=: Moltiplica e assegna un valore.
    /=: Divide e assegna un valore.
    %=: Calcola il resto della divisione e assegna un valore.
    **=: Esegue l'elevamento a potenza e assegna un valore.
    <<=: Sposta a sinistra e assegna un valore.
    >>=: Sposta a destra e assegna un valore.
    >>>=: Sposta a destra (non conserva il segno) e assegna un valore.


2 Opeatori aritmetici: 

   +: Addizione o concatenazione di stringhe.
    -: Sottrazione.
    *: Moltiplicazione.
    /: Divisione.
    %: Resto della divisione.
    **: Elevamento a potenza.
    ++: Incremento di 1.
    --: Decremento di 1. 


3 Operatori di confronto:

    ==: Uguaglianza (valore).
    ===: Uguaglianza stretta (valore e tipo).
    !=: Diverso (valore).
    !==: Diverso stretto (valore e tipo).
    >: Maggiore di.
    <: Minore di.
    >=: Maggiore o uguale a.
    <=: Minore o uguale a.


4 Operatori logici:

    &&: AND logico.
    ||: OR logico.
    !: NOT logico.


5 Operatori di accesso agli elementi:

    .: Accede ad una proprietà di un oggetto.
    []: Accede ad un elemento di un array o una proprietà di un oggetto usando una chiave dinamica.


*/